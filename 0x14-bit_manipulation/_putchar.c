#include "main.h"
#include <stdio.h>

/**
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success, it returns the character written as an unsigned char cast to an int.
 * On failure, it returns EOF (which is defined as -1).
 */
int _putchar(char c)
{
	return putchar(c);
}
